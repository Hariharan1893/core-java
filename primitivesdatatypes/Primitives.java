package primitivesdatatypes;

public class Primitives {

  public static void main(String[] args) {

    // Primitive data types in Java

    // Integer data type
    // minimum value: -2,147,483,648 maximum value: 2,147,483,647
    int integerValue = 42;
    System.out.println("Integer Value: " + integerValue);

    // Double data type
    // minimum value: 4.9E-324 maximum value: 1.7976931348623157E308
    double doubleValue = 3.14;
    System.out.println("Double Value: " + doubleValue);

    // Float data type
    // minimum value: 1.4E-45 maximum value: 3.4028235E38
    float floatValue = 2.718f;
    System.out.println("Float Value: " + floatValue);

    // Long data type
    // minimum value: -9,223,372,036,854,775,808 maximum value:
    // 9,223,372,036,854,775,807
    long longValue = 123456789L;
    System.out.println("Long Value: " + longValue);

    // Character data type
    // A single 16-bit Unicode character
    char charValue = 'z';
    System.out.println("Character Value: " + charValue);

    // Boolean data type
    // Represents one of two values: true or false
    boolean booleanValue = true;
    System.out.println("Boolean Value: " + booleanValue);

  }
}
